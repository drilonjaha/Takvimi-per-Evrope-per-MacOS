        case 12: return december
        default: return nil
        }
    }

    static func getPrayerTimes(for date: Date, city: City) -> DailyPrayerTimes? {
        // Only return hardcoded BIM data for Kosovo cities
        // Non-Kosovo cities should use the Aladhan API with their actual coordinates
        guard city.country == .kosovo else {
            return nil
        }

        let calendar = Calendar.current
        let month = calendar.component(.month, from: date)
        let day = calendar.component(.day, from: date)

        guard let monthData = getMonthData(for: month),
              let times = monthData[day] else {
            return nil
        }

        let offset = cityOffsets[city.id] ?? 0

        func parseTime(_ timeStr: String, offset: Int) -> Date? {
            let parts = timeStr.split(separator: ":")
            guard parts.count == 2,
                  let hour = Int(parts[0]),
                  let minute = Int(parts[1]) else { return nil }

            var components = calendar.dateComponents([.year, .month, .day], from: date)
            components.hour = hour
            components.minute = minute + offset
            components.second = 0
            return calendar.date(from: components)
        }

        guard let imsak = parseTime(times.0, offset: offset),
              let sunrise = parseTime(times.2, offset: offset),
              let dhuhr = parseTime(times.3, offset: offset),
              let asr = parseTime(times.4, offset: offset),
              let maghrib = parseTime(times.5, offset: offset),
              let isha = parseTime(times.6, offset: offset) else {
            return nil
        }

        // Fajr/Sabahu is automatically calculated as Imsak + 35 min in the initializer
        return DailyPrayerTimes(
            date: date,
            city: city,
            imsak: imsak,
            sunrise: sunrise,
            dhuhr: dhuhr,
            asr: asr,
            maghrib: maghrib,
            isha: isha,
            hijriDate: nil
        )
    }
